
public class TimeClass {
    
    protected int timeID;
    protected String time;

    public void setTimeID(int timeID) {
        this.timeID = timeID;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public int getTimeID() {
        return timeID;
    }

    public String getTime() {
        return time;
    }
    
}
